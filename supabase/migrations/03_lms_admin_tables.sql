-- =============================================================================
-- YagnaTech LMS — Supabase / Postgres schema bootstrap
-- File 03: lms_admin tables (Laravel-style LMS content + progress)
-- =============================================================================
-- Naming convention: snake_case (matches admin-service models).
-- All tables use created_at / updated_at (Laravel convention).
-- Cross-schema FKs to lucy_devdb are NOT enforced (matches the current
-- admin-service contract using the second Sequelize handle authDb).
-- NOTE: lms_admin.user_id columns store VARCHAR userIds from lucy_devdb.users
--       — DATABASE.md §9.7 flags the original BIGINT/STRING mismatch as a
--       latent bug. We use VARCHAR(255) here to remove the coercion risk.
-- =============================================================================

-- -----------------------------------------------------------------------------
-- users  (Laravel-style; distinct from lucy_devdb.users)
-- -----------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS lms_admin.users (
  id                INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  role              VARCHAR(100) NOT NULL,
  email             VARCHAR(255) NOT NULL UNIQUE,
  status            INTEGER,
  name              VARCHAR(255),
  phone             VARCHAR(255),
  website           VARCHAR(255),
  skills            TEXT,
  facebook          TEXT,
  twitter           VARCHAR(255),
  linkedin          VARCHAR(255),
  address           VARCHAR(255),
  college_name      VARCHAR(255),
  college_id        VARCHAR(255),  -- logical FK -> lucy_devdb.colleges.clgId
  about             TEXT,
  biography         TEXT,
  educations        TEXT,
  photo             VARCHAR(255),
  email_verified_at TIMESTAMPTZ,
  password          VARCHAR(255),  -- nullable in source; flagged in §9.7
  remember_token    VARCHAR(100),
  paymentkeys       TEXT,
  video_url         VARCHAR(255),
  created_at        TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at        TIMESTAMPTZ NOT NULL DEFAULT now()
);

