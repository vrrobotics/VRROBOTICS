-- -----------------------------------------------------------------------------
-- updated_at auto-touch trigger for every lms_admin table that has it
-- -----------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION lms_admin.touch_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at := now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DO $$
DECLARE r RECORD;
BEGIN
  FOR r IN
    SELECT table_name FROM information_schema.columns
     WHERE table_schema = 'lms_admin' AND column_name = 'updated_at'
     GROUP BY table_name
  LOOP
    EXECUTE format(
      'DROP TRIGGER IF EXISTS trg_touch_updated_at ON lms_admin.%I;
       CREATE TRIGGER trg_touch_updated_at BEFORE UPDATE ON lms_admin.%I
         FOR EACH ROW EXECUTE FUNCTION lms_admin.touch_updated_at();',
      r.table_name, r.table_name);
  END LOOP;
END $$;
